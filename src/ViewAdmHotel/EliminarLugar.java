    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        Query q;
        int i;
        String valor;
            JOptionPane.showMessageDialog(null, "Existen registros de seguimiento de actividad para este lugar,"
                    + "si elimina perderÃ¡ dichos registros","Aviso",JOptionPane.INFORMATION_MESSAGE );
            resp=  JOptionPane.showConfirmDialog(null,"Esta seguro que desea eliminar?", "Confirmar EliminaciÃ³n",JOptionPane.YES_NO_OPTION );
            if (resp==JOptionPane.YES_OPTION){
                EntityManagerFactory fact=Persistence.createEntityManagerFactory("proyectoPU");
                EntityManager ema= fact.createEntityManager();
                ema.getTransaction().begin();
                //eliminamos los lugares que tienen registros de seguimiento de actividad
                q=ema.createNativeQuery("SELECT * FROM seguimiento_actividad WHERE "
                        + "lugar= "
                        + "'"+tf_codigo.getText()+"'",SeguimientoActividad.class);
                List<SeguimientoActividad> s=q.getResultList();
                if(s.size()>=1){
                    for(i=0;i<s.size();i++){
                        ema.remove(s.get(i));
                    }
                    ema.flush();
                }
                //
                Lugar l=ema.find(Lugar.class,Integer.parseInt(tf_codigo.getText()) );
                valor=l.toString();//guardamos el objeto antes de eliminar
                ema.remove(l);
                AuditoriaSistema as=new AuditoriaSistema();
                as.setAccion("EliminaciÃ³n");
                as.setTabla("Lugar");
                Date fecha=new Date();
                DateFormat formato=new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
                as.setFechaHora(formato.format(fecha));
                as.setUsuario("nadie");
                as.setAntes(valor);
                as.setDespues("No hay modificaciones");
                ema.persist(as);
                ema.getTransaction().commit();
                ema.close();
                JOptionPane.showMessageDialog(null, "EliminaciÃ³n Exitosa");
                

            }
        
        this.dispose();
    }//GEN-LAST:event_btn_eliminarActionPerformed

